        <p>
            Ludovico Einaudi nacido en Turín el 23 de noviembre de 1955 Hijo del editor e intelectual italiano Giulio Einaudi y nieto de Luigi Einaudi. Empezó a tocar el piano de su madre de niño.
            En la actualidad, es un compositor y pianista italiano conocido y apreciado en todo el mundo por sus conciertos y canciones.
            Después de trasladarse a Milán, se graduó en composición en el Conservatorio Verdi y perfeccionó su música gracias al compositor Luciano Berio, su inolvidable maestro que recientemente ha rendido homenaje a la obra-homenaje Los Elementos.
            En el curso de su carrera trabajando con artistas de diferentes orígenes musicales, entre ellos: Ballaké Sissoko, Djivan Gasparijan, Mercan Dede, Robert y Ronald Lippok. Sus composiciones son ejemplo Transversal de la música, representan un puente entre las melodías clásicas y pop; Es un ejemplo del hecho de que era el único artista clásico de haber participado en la primera edición del Festival dell'Itunes (en 2007) al lado de los más populares artistas y bandas de rock en el mundo (entre muchos Oasis, Placebo y Franz Ferdinand).
